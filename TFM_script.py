def circular_mae_deg(pred: torch.Tensor,
                     target: torch.Tensor,
                     mask: torch.Tensor) -> float:
    """
    Calcula el MAE circular en grados entre predicciones
    y targets, solo sobre posiciones enmascaradas.

    La distancia circular entre a y b se obtiene como
    |atan2(sin(a-b), cos(a-b))|, que devuelve valores en
    [0, π] independientemente de la periodicidad.

    Parámetros
    ----------
    pred   : tensor [N] — ángulos predichos (radianes)
    target : tensor [N] — ángulos reales (radianes)
    mask   : tensor [N] — True donde hay ángulo real

    Retorna
    -------
    float — MAE circular en grados
    """
    if not mask.any():
        return float('nan')
    diff = torch.atan2(
        torch.sin(pred[mask] - target[mask]),
        torch.cos(pred[mask] - target[mask])
    )
    return torch.abs(diff).mean().item() * (180 / np.pi)


