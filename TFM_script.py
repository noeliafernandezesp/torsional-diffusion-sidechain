# ============================================================
# BLOQUE 18  — Proceso Reverse: Muestreo de Cadenas Laterales
#              mediante Difusión Torsional Iterativa
# ============================================================
# Implementa el proceso reverse (denoising iterativo):
# partiendo de ángulos chi uniformemente distribuidos (ruido
# puro), aplica T pasos de denoising usando el modelo entrenado
# para recuperar conformaciones de cadenas laterales plausibles.
#
# Algoritmo DDIM simplificado para ángulos:
#   Para t = T, T-1, ..., 1:
#     1. El modelo predice χ_0 a partir de χ_t y t
#     2. Re-añadimos ruido con κ_{t-1} (más concentrado que κ_t)
#        para simular el paso previo de la cadena de Markov
#     3. Al llegar a t=0, χ_0 predicho es la conformación final
#
# Este bloque produce el primer conjunto de conformaciones
# "recuperadas".
# ============================================================
# Guard: si las predicciones de test ya existen en Drive y
# FORCE_INFERENCE = False, se cargan directamente.
# Generar las K=10 muestras del Bloque 22 (calibración)
# es el paso más lento de la tercera fase del proyecto. Evitarlo
# en re-ejecuciones del notebook de evaluación ahorra 20–30 minutos.
# ============================================================
import torch
import numpy as np

@torch.no_grad()
def reverse_diffusion(model, batch, device: torch.device,
                      n_steps: int = 20) -> torch.Tensor:
    """
    Genera conformaciones de cadenas laterales para un batch
    de proteínas mediante el proceso reverse de difusión.

    Para reducir el tiempo de inferencia en Colab, usamos
    n_steps << T (muestreo acelerado DDIM): en lugar de los
    100 pasos del proceso forward, muestreamos n_steps pasos
    equiespaciados en sentido inverso.

    Parámetros
    ----------
    model   : ChiGNN    — modelo entrenado
    batch   : Batch     — grafos de proteínas (sin ángulos)
    device  : torch.device
    n_steps : int       — pasos de denoising (default: 20)

    Retorna
    -------
    torch.Tensor [N_total, 4] — ángulos chi predichos en radianes
    """
    model.eval()
    batch  = batch.to(device)
    N      = batch.num_nodes

    # Paso 1: Inicializar con ruido uniforme sobre el círculo
    # VM(μ, κ=0) ≈ distribución uniforme → muestreamos aleatoriamente
    chi_t = torch.tensor(
        np.random.uniform(-np.pi, np.pi, size=(N, 4)),
        dtype=torch.float, device=device
    )

    # Pasos de tiempo en orden decreciente (T → 1)
    timesteps = torch.linspace(T, 1, n_steps).long()

    for t_int in timesteps:
        t_val      = t_int.item()
        t_norm     = torch.full((N, 1), t_val / T,
                                dtype=torch.float, device=device)

        # Paso 2: El modelo predice χ_0 desde χ_t
        chi_0_pred = model(
            x          = batch.x,
            aa_type    = batch.aa_type,
            edge_index = batch.edge_index,
            chi_noisy  = chi_t,
            t_node     = t_norm
        )   # [N, 4] en [-π, π]

        # Paso 3: Re-añadir ruido para simular χ_{t-1}
        # Si es el último paso (t=1), no re-añadimos ruido
        if t_val > 1:
            t_prev  = t_val - (T / n_steps)
            kappa_prev = get_kappa(int(t_prev))
            mask_all   = torch.ones(N, 4, dtype=torch.bool)
            chi_t      = forward_diffusion(
                torch.rad2deg(chi_0_pred), mask_all, int(t_prev)
            ).to(device)
        else:
            chi_t = chi_0_pred

    return chi_t   # Conformaciones finales predichas

