def calcular_angulos_chi(pdb_file):
    """
    Extrae ángulos chi y coordenadas del backbone de un archivo PDB.
    Los residuos se ordenan por posición en la cadena para garantizar
    que el edge_index del Bloque 9 conecte vecinos reales.

    Parámetros
    ----------
    pdb_file : str
        Ruta al archivo .pdb descargado de PDB-REDO.

    Retorna
    -------
    list[dict]
        Lista de diccionarios, uno por residuo válido, con columnas:
        pdb_id, residue_name, residue_idx, N_x/y/z, CA_x/y/z,
        C_x/y/z, chi1, chi2, chi3, chi4.
    """
    parser    = Bio.PDB.PDBParser(QUIET=True)  # QUIET suprime warnings de PDB
    structure = parser.get_structure('protein', pdb_file)
    data      = []

    for model in structure:
        for chain in model:

            # Orden secuencial por número de residuo (ej: 1,2,3,...,148)
            # Imprescindible para que las aristas del grafo sean correctas
            residuos_ordenados = sorted(chain, key=lambda r: r.get_id()[1])

            for residue in residuos_ordenados:

                # ── Filtro 1: Solo aminoácidos con cadena lateral ─────
                # GLY: sin CB → chi1 imposible
                # ALA: CB terminal → chi1 indefinido
                if not Bio.PDB.is_aa(residue) or residue.get_resname() in ['GLY', 'ALA']:
                    continue

                # ── Filtro 2: Backbone completo obligatorio ───────────
                # N, CA, C → coordenadas del nodo del grafo (features X)
                # CB       → primer átomo de la cadena lateral (chi1)
                try:
                    n_vec  = residue['N'].get_vector()
                    ca_vec = residue['CA'].get_vector()
                    c_vec  = residue['C'].get_vector()
                    cb_vec = residue['CB'].get_vector()
                except KeyError:
                    continue  # Residuo sin backbone completo: descartado

                res_name = residue.get_resname()
                res_id   = residue.get_id()[1]

                # ── Features X: coordenadas 3D del backbone ───────────
                # 9 valores por residuo: (N, CA, C) × (x, y, z)
                backbone_coords = {
                    'N_x':  n_vec[0],  'N_y':  n_vec[1],  'N_z':  n_vec[2],
                    'CA_x': ca_vec[0], 'CA_y': ca_vec[1], 'CA_z': ca_vec[2],
                    'C_x':  c_vec[0],  'C_y':  c_vec[1],  'C_z':  c_vec[2]
                }

                # ── Targets Y: ángulos chi (diedros de cadena lateral) ─
                # Estructura anidada: chi_n solo se calcula si chi_{n-1}
                # ha sido posible, reflejando la dependencia atómica real.
                # Los átomos gamma/delta/epsilon/zeta varían por residuo:
                #   CG  → la mayoría (LEU, LYS, ARG...)
                #   CG1 → ILE, VAL
                #   OG  → SER       OG1 → THR
                #   SG  → CYS, MET
                chis = [None, None, None, None]
                try:
                    # chi1: N–CA–CB–CG (o equivalente gamma)
                    gamma_atoms = [a for a in residue
                                   if a.get_name() in ['CG','CG1','OG','SG','OG1']]
                    if gamma_atoms:
                        g_vec   = gamma_atoms[0].get_vector()
                        chis[0] = np.degrees(
                            Bio.PDB.calc_dihedral(n_vec, ca_vec, cb_vec, g_vec))

                        # chi2: CA–CB–CG–CD (o equivalente delta)
                        delta_atoms = [a for a in residue
                                       if a.get_name() in ['CD','CD1','SD','OD1','ND1']]
                        if delta_atoms:
                            d_vec   = delta_atoms[0].get_vector()
                            chis[1] = np.degrees(
                                Bio.PDB.calc_dihedral(ca_vec, cb_vec, g_vec, d_vec))

                            # chi3: CB–CG–CD–CE (o equivalente epsilon)
                            epsilon_atoms = [a for a in residue
                                             if a.get_name() in ['CE','NZ','OE1','NE']]
                            if epsilon_atoms:
                                e_vec   = epsilon_atoms[0].get_vector()
                                chis[2] = np.degrees(
                                    Bio.PDB.calc_dihedral(cb_vec, g_vec, d_vec, e_vec))

                                # chi4: CG–CD–CE–CZ (solo ARG y LYS)
                                zeta_atoms = [a for a in residue
                                              if a.get_name() in ['CZ','NH1']]
                                if zeta_atoms:
                                    z_vec   = zeta_atoms[0].get_vector()
                                    chis[3] = np.degrees(
                                        Bio.PDB.calc_dihedral(g_vec, d_vec, e_vec, z_vec))

                except Exception:
                    # Si falla el cálculo geométrico (colinealidad, etc.)
                    # dejamos los chis como None; el Bloque 9 lo gestionará
                    pass

                # ── Consolidación de la fila ──────────────────────────
                row = {
                    'pdb_id':       os.path.basename(pdb_file).replace('.pdb', ''),
                    'residue_name': res_name,
                    'residue_idx':  res_id,
                    **backbone_coords,
                    'chi1': chis[0], 'chi2': chis[1],
                    'chi3': chis[2], 'chi4': chis[3]
                }
                data.append(row)

    return data

