#
# Nomenclatura de PDB-REDO:
#   https://pdb-redo.eu/db/{pid}/{pid}_final.pdb
#   '{pid}_final.pdb' es la estructura completamente re-refinada,
#   lista para análisis estructural. Es el archivo de mayor
