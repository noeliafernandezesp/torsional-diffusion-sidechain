"""Extracción de ángulos $\chi_1$, $\chi_2$, $\chi_3$ y $\chi_4$

Vamos a realizar la extracción de datos para el dataset.

-El ángulo $\chi_1$ se define por la rotación del enlace entre el Carbono Alfa  $C\alpha$ y el carbono Beta $C\beta$. Para medirlo necesitamos las coordenadas de 4 átomos: $N$, $C\alpha$, $C\beta$ y $C\gamma$.

-El ángulo $\chi_2$ se define por la rotación del enlace entre el Carbono Beta  $C\beta$ y el carbono Gamma $C\gamma$. Para medirlo necesitamos las coordenadas de 4 átomos: $C\alpha$, $C\beta$, $C\gamma$ y $C\delta$.

-El ángulo $\chi_3$ se define por la rotación del enlace entre el Carbono Gamma  $C\gamma$ y el carbono delta $C\delta$. Para medirlo necesitamos las coordenadas de 4 átomos: $C\beta$, $C\gamma$, $C\delta$ y $C\epsilon$.

-El ángulo $\chi_4$ se define por la rotación del enlace entre el Carbono Delta  $C\delta$ y el carbono Epsilon $C\epsilon$. Para medirlo necesitamos las coordenadas de 4 átomos: $C\gamma$, $C\delta$, $C\epsilon$ y $C\zeta$.
"""

# ============================================================
# BLOQUE 8 — Extracción de ángulos chi y coordenadas del backbone
# ============================================================
# Este bloque es el núcleo del pipeline de datos de la fase 1. Para cada
# estructura descargada, extrae dos tipos de información:
#
#   Features (X) — Coordenadas 3D del backbone (N, CA, C):
#     Son la entrada de la GNN. Representan la geometría de la
#     cadena principal, que se asume conocida y fija. El modelo
#     aprenderá a predecir las cadenas laterales condicionado
#     a estas coordenadas.
#
#   Targets (Y) — Ángulos diedros chi1, chi2, chi3, chi4:
#     Son lo que el modelo de difusión torsional debe predecir.
#     Se calculan a partir de los átomos de la cadena lateral
#     usando la función calc_dihedral de Biopython.
#
# Decisiones de filtrado justificadas:
#
#   GLY excluida → no tiene CB, por tanto no tiene ningún chi.
#   ALA excluida → tiene CB pero no tiene cadena lateral más allá;
#                  chi1 es geométricamente indefinido.
#   Backbone incompleto → si falta N, CA, C o CB, el residuo
#                  no puede usarse como nodo de grafo (sin coords)
#                  ni para calcular chi1 (sin CB). Se descarta.
#
# Orden secuencial garantizado:
#   sorted(chain, key=lambda r: r.get_id()[1]) ordena los residuos
#   por su número de secuencia antes de procesarlos. Sin esto,
#   el edge_index del Bloque 9 conectaría residuos en orden
#   arbitrario de lectura del PDB, no en orden peptídico real.
#
# Lógica de cálculo de chi (anidada por disponibilidad):
#   chi1 requiere: N, CA, CB, CG (o equivalente)
#   chi2 requiere: chi1 calculado + CD (o equivalente)
#   chi3 requiere: chi2 calculado + CE (o equivalente)
#   chi4 requiere: chi3 calculado + CZ (o equivalente)
#   Si un átomo intermedio no existe, los chi superiores son None.
#   Esto es biológicamente correcto: SER solo tiene chi1,
#   GLU tiene chi1-chi3, ARG tiene chi1-chi4.
#
# ============================================================
# Guard: si el CSV curado ya existe en Drive y
