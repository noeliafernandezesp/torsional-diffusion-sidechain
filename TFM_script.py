"""Bloque 15: Arquitectura ChiGNN"""

# ============================================================
# BLOQUE 15 — Definición de la Arquitectura GNN
#              (Red Neuronal de Grafos para Denoising)
# ============================================================
# Define el modelo de denoising: una GNN que, dado un grafo
# proteico con ángulos ruidosos χ_t y el paso de tiempo t,
# predice los ángulos originales χ_0.
#
# Arquitectura: ChiGNN
#   Entrada por nodo:
#     · 9 features de backbone (N, Cα, C coords)
#     · 1 embedding de aminoácido (dim 16)
#     · 4 ángulos chi ruidosos (senos y cosenos → 8 features)
#     · 1 feature de tiempo t normalizado
#     → Total: 9 + 16 + 8 + 1 = 34 features de entrada
#
#   Procesamiento:
#     · 4 capas GCNConv (Graph Convolutional Network)
#     · Función de activación: SiLU (suave, sin "dying ReLU")
#     · Batch normalization entre capas
#
#   Salida por nodo:
#     · 4 valores en [-π, π] (uno por cada ángulo chi)
#     · Activación tanh × π para forzar rango angular correcto
#
# Por qué GCNConv y no EGNN:
#   GCNConv es suficiente porque nuestras features de entrada
#   ya incluyen las coordenadas del backbone: el modelo puede
#   aprender las relaciones geométricas a partir de ellas.
#   EGNN añadiría equivariancia estricta pero requeriría 3×
#   más memoria y es más difícil de depurar en Colab.
# ============================================================

import torch
import torch.nn as nn
from torch_geometric.nn import GCNConv, global_mean_pool

class ChiGNN(nn.Module):
    """
    Red Neuronal de Grafos para denoising de ángulos chi.

    Parámetros
    ----------
    hidden_dim : int — dimensión de los embeddings ocultos (default: 128)
    n_layers   : int — número de capas de paso de mensajes (default: 4)
    aa_embed   : int — dimensión del embedding de aminoácido (default: 16)
    """

    def __init__(self, hidden_dim: int = 128,
                       n_layers:   int = 4,
                       aa_embed:   int = 16):
        super().__init__()

        self.hidden_dim = hidden_dim
        self.n_layers   = n_layers

        # ── Embedding de tipo de aminoácido ───────────────────
        # 21 tipos (20 estándar + 1 desconocido) → vector de dim aa_embed
        # Aprendible: el modelo descubrirá qué aminoácidos tienen
        # perfiles rotaméricos similares.
        self.aa_embedding = nn.Embedding(21, aa_embed)

        # ── Dimensión de entrada total ─────────────────────────
        # 9 (backbone) + aa_embed (aminoácido) + 8 (sin/cos de 4 chi) + 1 (t)
        input_dim = 9 + aa_embed + 8 + 1

        # ── Proyección inicial al espacio oculto ──────────────
        self.input_proj = nn.Sequential(
            nn.Linear(input_dim, hidden_dim),
            nn.SiLU()
        )

        # ── Capas de convolución en grafo ─────────────────────
        # Cada capa agrega información de los residuos vecinos.
        # BatchNorm1d estabiliza el entrenamiento normalizando
        # las activaciones por batch.
        self.conv_layers = nn.ModuleList([
            GCNConv(hidden_dim, hidden_dim)
            for _ in range(n_layers)
        ])
        self.norms = nn.ModuleList([
            nn.BatchNorm1d(hidden_dim)
            for _ in range(n_layers)
        ])
        self.acts = nn.ModuleList([
            nn.SiLU() for _ in range(n_layers)
        ])

        # ── Cabeza de predicción ──────────────────────────────
        # MLP de dos capas que mapea el embedding del nodo
        # a 4 valores angulares en [-π, π].
        self.output_head = nn.Sequential(
            nn.Linear(hidden_dim, hidden_dim // 2),
            nn.SiLU(),
            nn.Linear(hidden_dim // 2, 4)
        )

    def encode_angles(self, chi: torch.Tensor) -> torch.Tensor:
        """
        Codifica los ángulos chi como pares (sin, cos) para
        preservar la periodicidad en la representación de entrada.

        Un ángulo θ se representa como [sin(θ), cos(θ)], lo que
        garantiza que ángulos cercanos en el círculo tengan
        representaciones similares (evita el salto discontinuo
        en ±π que tendría θ representado directamente).

        Parámetros
        ----------
        chi : tensor [N, 4] — ángulos en radianes

        Retorna
        -------
        tensor [N, 8] — codificación sin/cos de los 4 ángulos
        """
        return torch.cat([torch.sin(chi), torch.cos(chi)], dim=-1)

    def forward(self, x, aa_type, edge_index, chi_noisy, t_node):
        """
        Paso forward de la red.

        Parámetros
        ----------
        x          : tensor [N, 9]   — coordenadas backbone
        aa_type    : tensor [N]      — índice de aminoácido (long)
        edge_index : tensor [2, E]   — aristas del grafo
        chi_noisy  : tensor [N, 4]   — ángulos ruidosos χ_t (radianes)
        t_node     : tensor [N, 1]   — paso de tiempo normalizado

        Retorna
        -------
        tensor [N, 4] — ángulos predichos χ_0 en [-π, π]
        """
        # 1. Construir el vector de features del nodo
        aa_emb    = self.aa_embedding(aa_type)          # [N, aa_embed]
        chi_enc   = self.encode_angles(chi_noisy)       # [N, 8]
        node_feat = torch.cat([x, aa_emb, chi_enc, t_node], dim=-1)  # [N, input_dim]

        # 2. Proyección inicial
        h = self.input_proj(node_feat)                  # [N, hidden_dim]

        # 3. Paso de mensajes iterativo con conexiones residuales
        #    Las skip connections (h = h + h_new) evitan el
        #    desvanecimiento del gradiente en capas profundas.
        for conv, norm, act in zip(self.conv_layers, self.norms, self.acts):
            h_new = conv(h, edge_index)
            h_new = norm(h_new)
            h_new = act(h_new)
            h     = h + h_new    # Conexión residual

        # 4. Predicción de ángulos
        #    tanh × π fuerza la salida al rango [-π, π],
        #    que es el dominio válido de los ángulos diedros.
        out = self.output_head(h)           # [N, 4]  sin restricción
        out = torch.tanh(out) * torch.pi    # [N, 4]  en [-π, π]
        return out


