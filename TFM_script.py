import os, requests

def descargar_pdb_redo(lista_ids):
    """
    Descarga los archivos '_final.pdb' de PDB-REDO para cada ID
    de la lista y los guarda en Drive. Omite los ya descargados.

    Parámetros
    ----------
    lista_ids : list[str]
        Lista de identificadores PDB de 4 caracteres obtenida
        (ej: ['1A1X', '1B2Y', ...]).

    Retorna
    -------
    list[str]
        Lista de rutas locales (en Drive) de los archivos
        descargados correctamente.
    """

    # URL raíz del repositorio de PDB-REDO
    base_url  = "https://pdb-redo.eu/db"
    rutas_finales = []

    print("Iniciando descarga optimizada de PDB-REDO...")

    for pdb_id in lista_ids:

        # PDB-REDO usa IDs en minúsculas en su estructura de URLs
        pid = pdb_id.lower()
        url = f"{base_url}/{pid}/{pid}_final.pdb"

        # Ruta de destino en Drive
        file_path = f"{PDB_DIR}/{pid}.pdb"

        # ── Comprobación de caché ─────────────────────────────
        # Si el archivo ya existe en Drive (sesión reiniciada),
        # lo añadimos directamente sin volver a descargarlo
        if os.path.exists(file_path):
            rutas_finales.append(file_path)
            continue

        # ── Descarga con manejo de errores ───────────────────
        try:
            # timeout=10s evita bloqueos ante servidores lentos
            r = requests.get(url, timeout=10)

            if r.status_code == 200:
                # Escritura en modo binario ('wb') para preservar
                # el formato exacto del archivo .pdb
                with open(file_path, "wb") as f:
                    f.write(r.content)
                rutas_finales.append(file_path)
                print(f"✅ {pid} descargado.")
            else:
                # 404 más frecuente: estructura sin versión en PDB-REDO
                print(f"⚠️  {pid} no disponible en PDB-REDO, omitiendo...")

        except Exception as e:
            # Captura timeouts, errores de red y problemas de escritura
            print(f"❌ Error descargando {pid}: {e}")

    return rutas_finales

