"""Bloque 22: Calibración de confianza (Spearman + deciles)"""

# ============================================================
# BLOQUE 22 — Análisis de Calibración de Confianza
# ============================================================
# Evalúa si la incertidumbre del modelo (medida como la
# dispersión de múltiples muestras del proceso reverse) está
# bien calibrada respecto al error real.
#
# Idea central:
#   Si el modelo es bien calibrado, las predicciones con alta
#   varianza entre muestras (alta incertidumbre) deberían
#   corresponder a residuos con alto error real (MAE alto), y
#   las predicciones con baja varianza deberían tener MAE bajo.
#
# Procedimiento:
#   1. Para cada residuo del test, generamos K=10 muestras
#      independientes del proceso reverse.
#   2. Calculamos la varianza circular de las K muestras como
#      medida de incertidumbre del modelo.
#   3. Correlacionamos la varianza circular con el MAE real
#      usando el coeficiente de Spearman (apropiado para
#      datos no normales, como es el caso de los errores
#      angulares).
#   4. Visualizamos la relación mediante un gráfico de
#      dispersión con línea de tendencia y bandas de
#      percentiles.
# ============================================================

import torch
import numpy as np
import matplotlib.pyplot as plt
from scipy import stats

K_SAMPLES = 10   # Número de muestras del proceso reverse por residuo

# ── 1. Generar K muestras para cada residuo del test ─────────
print(f"🔄 Generando {K_SAMPLES} muestras por residuo (proceso reverse)...")
print(f"   Esto puede tardar {K_SAMPLES * 2}–{K_SAMPLES * 3} minutos en T4.")

all_samples = []   # Lista de K tensores [N_test, 4]

test_loader_calib = DataLoader(checkpoint['test'], batch_size=4, shuffle=False)

for k in range(K_SAMPLES):
    samples_k = []
    for batch in test_loader_calib:
        batch    = batch.to(device)
        chi_pred = reverse_diffusion(model, batch, device, n_steps=20)
        samples_k.append(chi_pred.cpu())
    all_samples.append(torch.cat(samples_k, dim=0))  # [N_test, 4]
    print(f"  Muestra {k+1}/{K_SAMPLES} completada.")

# Stack: [K, N_test, 4]
samples_stack = torch.stack(all_samples, dim=0)

# ── 2. Varianza circular por residuo ─────────────────────────
# Varianza circular de Von Mises: V = 1 - |mean(exp(iθ))|
# donde i es la unidad imaginaria. Este estimador es
# invariante a la periodicidad y devuelve valores en [0, 1]:
#   V ≈ 0 → muestras muy concentradas (baja incertidumbre)
#   V ≈ 1 → muestras uniformes sobre el círculo (máxima incertidumbre)

sin_mean = torch.sin(samples_stack).mean(dim=0)  # [N_test, 4]
cos_mean = torch.cos(samples_stack).mean(dim=0)  # [N_test, 4]
circ_var = 1 - torch.sqrt(sin_mean**2 + cos_mean**2)  # [N_test, 4]

# ── 3. Error real (MAE circular) por residuo ──────────────────
# Usamos la media de las K muestras como predicción final
chi_mean_pred = torch.atan2(sin_mean, cos_mean)  # [N_test, 4]

error_por_residuo = torch.abs(torch.atan2(
    torch.sin(chi_mean_pred - chi_targets),
    torch.cos(chi_mean_pred - chi_targets)
)) * (180 / np.pi)  # En grados

# ── 4. Correlación de Spearman y visualización para χ₁ ───────
mask_chi1 = masks_test[:, 0]
var_chi1  = circ_var[mask_chi1, 0].numpy()
err_chi1  = error_por_residuo[mask_chi1, 0].numpy()

# Correlación de Spearman (no paramétrica)
rho, p_val = stats.spearmanr(var_chi1, err_chi1)

print(f"\n📊 Calibración de Confianza — χ₁")
print(f"{'─'*45}")
print(f"  Correlación de Spearman (ρ) : {rho:.4f}")
print(f"  p-valor                     : {p_val:.4e}")
if rho > 0.3 and p_val < 0.05:
    print(f"  ✅ Correlación positiva significativa:")
    print(f"     El modelo está bien calibrado.")
elif rho > 0 and p_val < 0.05:
    print(f"  ⚠️  Correlación débil pero significativa.")
else:
    print(f"  ❌ Sin correlación significativa → modelo mal calibrado.")
print(f"{'─'*45}")

# ── 5. Gráfico de dispersión con percentiles ──────────────────
fig, axes = plt.subplots(1, 2, figsize=(14, 5))

# Panel izquierdo: scatter varianza vs. error
# Submuestreamos para no saturar el gráfico (máx 3000 puntos)
n_plot = min(3000, len(var_chi1))
idx_plot = np.random.choice(len(var_chi1), n_plot, replace=False)

axes[0].scatter(var_chi1[idx_plot], err_chi1[idx_plot],
                alpha=0.3, s=8, color='steelblue', rasterized=True)

# Línea de tendencia (regresión lineal)
z = np.polyfit(var_chi1, err_chi1, 1)
p = np.poly1d(z)
x_line = np.linspace(var_chi1.min(), var_chi1.max(), 100)
axes[0].plot(x_line, p(x_line), color='red', linewidth=2,
             label=f'Tendencia (ρ={rho:.3f}, p={p_val:.2e})')

axes[0].set_xlabel('Varianza circular (incertidumbre del modelo)', fontsize=11)
axes[0].set_ylabel('Error real MAE χ₁ (°)', fontsize=11)
axes[0].set_title('Calibración de Confianza\n(Varianza circular vs. Error real)',
                  fontsize=12)
axes[0].legend(fontsize=9)
axes[0].grid(alpha=0.3)

# Panel derecho: error medio por decil de incertidumbre
n_deciles   = 10
