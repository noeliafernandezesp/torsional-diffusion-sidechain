# ============================================================
# BLOQUE 10 — Visualización de la Distribución del Ángulo χ₁
#              mediante Rose Plot (Histograma Polar)
# ============================================================
# Genera un histograma circular (rose plot) del ángulo diedro
# χ₁ sobre todos los residuos del dataset. Al ser χ₁ un ángulo
# periódico, la representación polar es estadísticamente más
# honesta que un histograma cartesiano: evita el artefacto de
# corte en los extremos (±180°) y permite ver de un vistazo
# los tres estados rotaméricos canónicos: gauche⁻ (−60°),
# trans (180°) y gauche⁺ (+60°).
# ============================================================

def plot_rose_chi1(df):

    # ----------------------------------------------------------
    # 1. Limpieza del DataFrame
    #    Se eliminan las filas sin valor de χ₁ (NaN), que
    #    corresponden a residuos sin cadena lateral susceptible
    #    de tener este ángulo (Gly, Ala). Ignorarlos evita
    #    errores en np.histogram y sesgos en el conteo.
    # ----------------------------------------------------------
    df_clean = df.dropna(subset=['chi1'])

    # ----------------------------------------------------------
    # 2. Conversión de grados a radianes en [0, 2π]
    #    Matplotlib polar trabaja en radianes. El operador % 360
    #    normaliza previamente valores fuera del rango [0°, 360°)
    #    (p. ej. −60° → 300°), garantizando que todos los ángulos
    #    caigan dentro del círculo completo.
    # ----------------------------------------------------------
    angles_rad = np.deg2rad(df_clean['chi1'] % 360)

    # ----------------------------------------------------------
    # 3. Configuración del lienzo polar
    #    projection='polar' activa el sistema de coordenadas
    #    circular de Matplotlib; el tamaño cuadrado (8×8)
    #    evita que el círculo aparezca distorsionado.
    # ----------------------------------------------------------
    plt.figure(figsize=(8, 8))
    ax = plt.subplot(111, projection='polar')

    # ----------------------------------------------------------
    # 4. Cálculo del histograma angular
    #    36 bins de 10° cada uno (360° / 10° = 36) nos dan
    #    resolución suficiente para distinguir los estados
    #    rotaméricos sin sobresegmentar la distribución.
    #    np.histogram devuelve los conteos por bin; las anchuras
    #    (widths) se calculan como la diferencia entre bordes
    #    consecutivos para que ax.bar rellene cada sector exacto.
    # ----------------------------------------------------------
    bins   = np.linspace(0.0, 2 * np.pi, 37)   # 37 bordes → 36 bins
    counts, _ = np.histogram(angles_rad, bins=bins)
    widths    = np.diff(bins)                   # Anchura uniforme ≈ 0.175 rad

    # ----------------------------------------------------------
    # 5. Dibujo de las barras polares
    #    bins[:-1] posiciona cada barra en el borde izquierdo
    #    de su sector. bottom=0.0 hace que las barras arranquen
    #    desde el centro, maximizando la legibilidad de la
    #    magnitud relativa entre rotámeros.
    # ----------------------------------------------------------
    ax.bar(bins[:-1], counts, width=widths, bottom=0.0,
           edgecolor='black', color='lightgreen', alpha=0.7)

    # ----------------------------------------------------------
    # 6. Ajustes de orientación y etiquetas
    #    set_theta_zero_location("N"): el 0° queda arriba
    #    set_theta_direction(-1): sentido horario, coherente
    #      con la representación de ángulos diedros en PDB.
    #    Las etiquetas cada 45° permiten leer directamente los
    #    tres estados rotaméricos canónicos de χ₁:
    #      gauche⁻ ≈ 300° | trans ≈ 180° | gauche⁺ ≈ 60°
    # ----------------------------------------------------------
    ax.set_theta_zero_location("N")
    ax.set_theta_direction(-1)

    ticks_rad = np.linspace(0, 2 * np.pi, 8, endpoint=False)
    ax.set_xticks(ticks_rad)
    ax.set_xticklabels(['0°', '45°', '90°', '135°', '180°', '225°', '270°', '315°'])

    plt.title(
        r"Distribución de Ángulos $\chi_1$ (Rose Plot)" +
        "\nEvidencia de Estados Rotaméricos",
        pad=20
    )
    plt.show()

