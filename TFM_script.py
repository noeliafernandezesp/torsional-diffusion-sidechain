# ============================================================
# BLOQUE 5 — Consulta a la API del PDB: selección del dataset
# ============================================================
# Consultamos la API REST del Protein Data Bank (RCSB) para
# obtener una lista de IDs de estructuras que cumplan los
# criterios de calidad del TFM. La selección no es arbitraria:
# cada filtro tiene una justificación estadística y estructural.
#
# Los 3 filtros aplicados en combinación AND:
#
#   Filtro 1 — Resolución < 2.0 Å
#     Las posiciones atómicas de las cadenas laterales solo son
#     fiables por debajo de este umbral. A resoluciones mayores,
#     los ángulos chi experimentales tienen un error asociado
#     que contaminaría el entrenamiento de la GNN.
#     (Criterio estándar en la literatura: Dunbrack, 2002)
#
#   Filtro 2 — Método: X-RAY DIFFRACTION
#     Excluimos NMR y Cryo-EM. La cristalografía de rayos X es el
#     gold standard para ángulos diedros de cadenas laterales.
#
#   Filtro 3 — Tipo de polímero: Protein
#     Sin este filtro, la query devuelve estructuras de ADN/ARN
#     que no tienen ángulos chi de aminoácidos.
#     Su inclusión contaminaría el dataset con filas vacías.
#
# La API devuelve los resultados paginados en bloques de 'rows'.
# Con limite=1000 obtenemos una muestra manejable;
# ============================================================

import requests

def obtener_lista_pdb(limite=1000):
    """
    Consulta la API REST de RCSB y devuelve una lista de IDs
    de estructuras proteicas de alta calidad.

    Parámetros
    ----------
    limite : int
        Número máximo de entradas a recuperar (default: 1000).

    Retorna
    -------
    list[str]
        Lista de identificadores PDB de 4 caracteres (ej: '1A1X').
    """

    # Endpoint de búsqueda avanzada de RCSB
    url = "https://search.rcsb.org/rcsbsearch/v2/query"

    # ── Construcción del query JSON ───────────────────────────
    # La API de RCSB usa un lenguaje de query propio basado en
    # nodos. El operador 'and' exige que los 3 filtros se cumplan
    # simultáneamente para que una entrada sea incluida.
    query = {
        "query": {
            "type": "group",
            "logical_operator": "and",
            "nodes": [
                {
                    # Filtro 1: Resolución < 2.0
                    "type": "terminal",
                    "service": "text",
                    "parameters": {
                        "attribute": "rcsb_entry_info.resolution_combined",
                        "operator":  "less",
                        "value":     2.0
                    }
                },
                {
                    # Filtro 2: Solo cristalografía de rayos X
                    "type": "terminal",
                    "service": "text",
                    "parameters": {
                        "attribute": "exptl.method",
                        "operator":  "exact_match",
                        "value":     "X-RAY DIFFRACTION"
                    }
                },
                {
                    # Filtro 3: Solo proteínas
                    "type": "terminal",
                    "service": "text",
                    "parameters": {
                        "attribute": "entity_poly.rcsb_entity_polymer_type",
                        "operator":  "exact_match",
                        "value":     "Protein"
                    }
                }
            ]
        },
        # Pedimos los identificadores de entrada, no los átomos
        "return_type": "entry",
        "request_options": {
            # Paginación: recuperamos 'limite' entradas desde la 0
            "paginate": {"start": 0, "rows": limite}
        }
    }

    # ── Ejecución de la consulta ──────────────────────────────
    response = requests.post(url, json=query)

    if response.status_code == 200:
        resultados = response.json().get('result_set', [])
        # Extraemos solo el identificador PDB de 4 caracteres
        return [item['identifier'] for item in resultados]
    else:
        print(f"❌ Error en la query: {response.status_code}")
        return []


