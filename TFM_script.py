# ============================================================
# BLOQUE 21 — Rose Plots Comparativos: Distribución Predicha
#              vs. Distribución Experimental
# ============================================================
# Genera histogramas polares (rose plots) superponiendo la
# distribución de ángulos χ predichos (modelo) y χ reales
# (dataset de test) para cada uno de los cuatro ángulos chi.
#
# Esta figura es la validación visual más importante del TFM:
# si el modelo ha aprendido correctamente la distribución
# rotamérica, los picos de la distribución predicha deben
# coincidir con los de la distribución experimental en las
# posiciones canónicas (−60°, +60°, 180°).
#
# Una distribución predicha demasiado uniforme indica que el
# modelo no ha aprendido los rotámeros. Una distribución
# predicha más estrecha que la experimental indica que el
# modelo es sobreconfiante (underdispersed).
# ============================================================

import matplotlib.pyplot as plt
import numpy as np
import torch

def rose_plot_comparativo(pred_rad: torch.Tensor,
                          target_rad: torch.Tensor,
                          mask: torch.Tensor,
                          titulo: str,
                          ax):
    """
    Dibuja un rose plot con dos histogramas superpuestos:
    el de las predicciones del modelo (azul) y el de los
    valores experimentales (verde, semitransparente).

    Parámetros
    ----------
    pred_rad   : tensor [N] — ángulos predichos (radianes)
    target_rad : tensor [N] — ángulos reales (radianes)
    mask       : tensor [N] — máscara de posiciones válidas
    titulo     : str        — título del subplot
    ax         : Axes       — eje polar de matplotlib
    """
    if not mask.any():
        return

    # Convertimos a grados en [0, 360) para histograma
    pred_deg   = (torch.rad2deg(pred_rad[mask]).numpy()   % 360)
    target_deg = (torch.rad2deg(target_rad[mask]).numpy() % 360)

    bins   = np.linspace(0, 2 * np.pi, 37)   # 36 bins de 10°
    widths = np.diff(bins)

    # Histograma de predicciones
    pred_rad_np   = np.deg2rad(pred_deg)
    counts_pred, _ = np.histogram(pred_rad_np, bins=bins)

    # Histograma de valores reales
    target_rad_np   = np.deg2rad(target_deg)
    counts_target, _ = np.histogram(target_rad_np, bins=bins)

    # Normalizamos para comparar distribuciones (no conteos absolutos)
    counts_pred   = counts_pred   / counts_pred.max()
    counts_target = counts_target / counts_target.max()

    # Dibujamos: experimental primero (fondo verde), predicho encima
    ax.bar(bins[:-1], counts_target, width=widths, bottom=0.0,
           color='lightgreen', alpha=0.6, edgecolor='darkgreen',
           linewidth=0.5, label='Experimental')
    ax.bar(bins[:-1], counts_pred, width=widths, bottom=0.0,
           color='steelblue', alpha=0.7, edgecolor='navy',
           linewidth=0.5, label='Predicho (ChiGNN)')

    # Configuración del eje polar
    ax.set_theta_zero_location("N")    # 0° arriba
    ax.set_theta_direction(-1)         # Sentido horario
    ticks = np.linspace(0, 2 * np.pi, 8, endpoint=False)
    ax.set_xticks(ticks)
    ax.set_xticklabels(['0°','45°','90°','135°','180°','225°','270°','315°'],
                       fontsize=8)
    ax.set_title(titulo, pad=15, fontsize=11, fontweight='bold')
    ax.legend(loc='upper right', bbox_to_anchor=(1.35, 1.1), fontsize=8)


# ── Figura con los 4 rose plots ───────────────────────────────
