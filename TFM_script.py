# ── 1. Bootstrap IC 95% para MAE de χ₁ ───────────────────────
def bootstrap_mae_ci(pred: torch.Tensor,
                     target: torch.Tensor,
                     mask: torch.Tensor,
                     n_boot: int = 1000,
                     ci: float = 95.0):
    """
    Calcula el intervalo de confianza bootstrap para el MAE
    circular de χ₁ mediante remuestreo con reemplazamiento.

    Parámetros
    ----------
    pred   : tensor [N] — predicciones (radianes)
    target : tensor [N] — valores reales (radianes)
    mask   : tensor [N] — máscara de posiciones válidas
    n_boot : int        — número de remuestras bootstrap
    ci     : float      — nivel de confianza (%) del intervalo

    Retorna
    -------
    tuple (mae_obs, ic_lower, ic_upper) en grados
    """
    pred_m   = pred[mask].numpy()
    target_m = target[mask].numpy()
    n        = len(pred_m)

    mae_obs  = np.abs(np.arctan2(
        np.sin(pred_m - target_m),
        np.cos(pred_m - target_m)
    )).mean() * (180 / np.pi)

    # Remuestreo bootstrap
    boot_maes = []
    for _ in range(n_boot):
        idx        = np.random.choice(n, n, replace=True)
        boot_diff  = np.abs(np.arctan2(
            np.sin(pred_m[idx] - target_m[idx]),
            np.cos(pred_m[idx] - target_m[idx])
        ))
        boot_maes.append(boot_diff.mean() * (180 / np.pi))

    alpha    = (100 - ci) / 2
    ic_lower = np.percentile(boot_maes, alpha)
    ic_upper = np.percentile(boot_maes, 100 - alpha)

    return mae_obs, ic_lower, ic_upper


