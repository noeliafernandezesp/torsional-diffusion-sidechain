"""Bloque 14: Preparación del batch para entrenamiento + pérdida circular"""

# ============================================================
# BLOQUE 14 — Preparación del Proceso de Difusión para
#              el Bucle de Entrenamiento
# ============================================================
# Implementa la función que el bucle de entrenamiento llamará
# en cada iteración: dado un batch de grafos con ángulos reales,
# devuelve los ángulos ruidosos χ_t y el paso de tiempo t
# muestreado aleatoriamente.
#
# Diseño de la función de pérdida:
#   El modelo aprende a predecir χ_0 (los ángulos reales) a
#   partir de χ_t y t. La pérdida es la distancia circular
#   entre el χ_0 predicho y el χ_0 real, calculada solo sobre
#   las posiciones con máscara=True.
#   Usar distancia circular y no MSE es crítico porque los
#   ángulos son periódicos: el error entre 179° y -179° es 2°,
#   no 358°.
# ============================================================

import torch
import numpy as np

def sample_t(batch_size: int) -> torch.Tensor:
    """
    Muestrea aleatoriamente un paso de tiempo t para cada grafo
    del batch. Muestrear t distinto por grafo equivale a
    entrenar simultáneamente en todos los niveles de ruido,
    lo que estabiliza el entrenamiento.

    Parámetros
    ----------
    batch_size : int — número de grafos en el batch

    Retorna
    -------
    torch.Tensor — enteros en [1, T], forma [batch_size], en CPU
    """
    # t=0 se excluye porque no añade ruido (no hay nada que aprender)
    return torch.randint(1, T + 1, (batch_size,))


def prepare_diffusion_batch(batch, device: torch.device):
    """
    Prepara un batch para el entrenamiento del modelo de difusión.
    Para cada grafo del batch:
      1. Muestrea un paso de tiempo t aleatorio
      2. Aplica el proceso forward → obtiene χ_t ruidoso
      3. Codifica t como feature adicional del nodo

    Parámetros
    ----------
    batch  : torch_geometric.data.Batch — batch de grafos
    device : torch.device               — CPU o CUDA

    Retorna
    -------
    dict con:
      'chi_noisy'  : tensor [N_total, 4]  — ángulos ruidosos (radianes)
      'chi_target' : tensor [N_total, 4]  — ángulos reales (radianes)
      'mask'       : tensor [N_total, 4]  — máscara booleana
      't_node'     : tensor [N_total, 1]  — t normalizado por nodo
      't_batch'    : tensor [batch_size]  — t entero por grafo
    """
    batch_size  = batch.num_graphs
    t_batch     = sample_t(batch_size).to(device)          # [B]    enteros
    t_norm      = t_batch.float() / T           # [B]    en [0,1]

    # Ahora batch.batch (GPU) y t_norm (GPU) están en el mismo device:
    # Expandimos t a nivel de nodo usando batch.batch:
    # batch.batch[i] = índice del grafo al que pertenece el nodo i
    t_per_node = t_norm[batch.batch].unsqueeze(1)      # [N, 1]

    # Convertimos ángulos target a radianes
    chi_target = torch.deg2rad(batch.y).to(device)            # [N, 4]
    mask       = batch.y_mask.to(device)                      # [N, 4]

    # Aplicamos el proceso forward con el t de cada grafo
    chi_noisy  = torch.zeros_like(chi_target)

    # t_batch_cpu: la función get_kappa y von_mises_sample usan
    # numpy internamente, así que necesitan el valor en CPU/Python.
    t_batch_cpu = t_batch.cpu()

    for graph_idx in range(batch_size):
        t_i        = t_batch[graph_idx].item()
        node_mask  = (batch.batch == graph_idx)  # Nodos de este grafo
        chi_i      = chi_target[node_mask]       # [n_i, 4]
        mask_i     = mask[node_mask]             # [n_i, 4]

        # Añadimos ruido solo a posiciones con ángulo real
        chi_noisy_i           = torch.zeros_like(chi_i)
        if mask_i.any():
          # von_mises_sample trabaja con numpy: movemos a CPU,
            # muestreamos, y devolvemos al device original.
            chi_noisy_i[mask_i] = von_mises_sample(
                chi_i[mask_i].cpu(), get_kappa(t_i)
            ).to(device)

        chi_noisy[node_mask]  = chi_noisy_i

    return {
        'chi_noisy':  chi_noisy,    # Input del modelo (ángulos ruidosos)
        'chi_target': chi_target,   # Target del modelo (ángulos reales)
        'mask':       mask,         # Solo estas posiciones cuentan en la pérdida
        't_node':     t_per_node,   # Feature de tiempo por nodo
        't_batch':    t_batch       # Paso de tiempo por grafo (para logs)
    }


def circular_loss(pred: torch.Tensor,
                  target: torch.Tensor,
                  mask: torch.Tensor) -> torch.Tensor:
    """
    Función de pérdida circular para ángulos diedros.
    Calcula el MAE circular solo sobre posiciones enmascaradas.

    La distancia circular entre dos ángulos a y b es:
      d(a, b) = 1 - cos(a - b)
    Este es el "cosine loss" circular, equivalente a la distancia
    geodésica en S¹ y diferenciable en todo el círculo.
    (Alternativa: |atan2(sin(a-b), cos(a-b))|, menos estable)

    Parámetros
    ----------
    pred   : tensor [N, 4] — ángulos predichos (radianes)
    target : tensor [N, 4] — ángulos reales (radianes)
    mask   : tensor [N, 4] — True donde hay ángulo real

    Retorna
    -------
    torch.Tensor — escalar, pérdida media sobre posiciones válidas
    """
    if not mask.any():
        return torch.tensor(0.0, requires_grad=True, device=pred.device)

    # Cosine loss: 0 cuando pred == target, 2 cuando están opuestos
    loss = 1 - torch.cos(pred[mask] - target[mask])
    return loss.mean()


