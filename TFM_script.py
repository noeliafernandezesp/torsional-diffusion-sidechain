def reconstruir_cadenas_laterales(pdb_path: str,
                                  chi1_predichos: np.ndarray,
                                  residue_indices: list) -> str:
    """
    Aplica los χ₁ predichos a los residuos de una proteína
    y devuelve la estructura modificada como string PDB.

    Parámetros
    ----------
    pdb_path       : str        — ruta al archivo .pdb original
    chi1_predichos : np.ndarray — valores de χ₁ predichos (rad)
    residue_indices: list       — índices de residuos válidos

    Retorna
    -------
    str — estructura modificada en formato PDB
    """
    parser    = Bio.PDB.PDBParser(QUIET=True)
    structure = parser.get_structure('prot', pdb_path)
    structure = copy.deepcopy(structure)

    residuos_validos = []
    for model in structure:
        for chain in model:
            for residue in sorted(chain, key=lambda r: r.get_id()[1]):
                if (Bio.PDB.is_aa(residue) and
                        residue.get_resname() not in ['GLY', 'ALA']):
                    residuos_validos.append(residue)

    # Aplicamos χ₁ predicho a cada residuo válido
    for idx, residue in enumerate(residuos_validos):
        if idx < len(chi1_predichos):
            apply_chi1_to_residue(residue, float(chi1_predichos[idx]))

    return structure_to_pdb_string(structure)


