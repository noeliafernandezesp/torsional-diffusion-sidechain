# ============================================================
# BLOQUE 12 — Proceso Forward de Difusión Torsional
#              (Adición de Ruido Von Mises a los Ángulos χ)
# ============================================================
# Implementa el proceso forward q(χ_t | χ_0): dado un ángulo
# chi real (χ_0), produce una versión "ruidosa" χ_t para un
# paso de tiempo t. El ruido sigue la distribución Von Mises
# —la análoga circular de la gaussiana— lo que garantiza que
# el resultado sigue siendo un ángulo válido en [-π, π].
#
# Intuición del proceso:
#   t=0   → κ muy alto   → distribución muy concentrada en χ_0
#           (el ángulo apenas ha cambiado)
#   t=T   → κ ≈ 0        → distribución uniforme sobre el círculo
#           (el ángulo es ruido puro, sin información)
#
# El modelo de denoising (Bloque 15) aprenderá a invertir
# exactamente este proceso: dado χ_t y t, predecir χ_0.
# ============================================================

import torch
import numpy as np

# ── Parámetros globales del proceso de difusión ───────────────
# T: número de pasos de difusión. 100 es suficiente para un
#    modelo lite; DiffPack original usa T=1000.
T        = 100

# κ_max: concentración inicial (t=0). Valor alto → ruido mínimo.
# κ_min: concentración final (t=T). Valor cercano a 0 → ruido
#        total (distribución uniforme sobre el círculo).
KAPPA_MAX = 10.0
KAPPA_MIN = 0.01

def get_kappa(t: int) -> float:
    """
    Devuelve el parámetro de concentración κ_t para el paso t.
    Usa un schedule lineal: κ disminuye de KAPPA_MAX a KAPPA_MIN
    a medida que t aumenta de 0 a T.

    Un schedule coseno (como en DDPM) sería más suave, pero el
    lineal es más sencillo de interpretar estadísticamente.

    Parámetros
    ----------
    t : int
        Paso de difusión actual (0 ≤ t ≤ T).

    Retorna
    -------
    float
        Valor de κ_t.
    """
    # Interpolación lineal: t=0 → κ_max, t=T → κ_min
    return KAPPA_MAX - (KAPPA_MAX - KAPPA_MIN) * (t / T)


def von_mises_sample(mu: torch.Tensor, kappa: float) -> torch.Tensor:
    """
    Muestrea ángulos de una distribución Von Mises VM(μ, κ).

    PyTorch no tiene Von Mises nativo hasta versiones recientes,
    así que usamos numpy y convertimos de vuelta a tensor.
    La muestra se normaliza al rango [-π, π] con torch.atan2.

    Parámetros
    ----------
    mu    : torch.Tensor — ángulos medios (en radianes), forma [N]
    kappa : float        — parámetro de concentración

    Retorna
    -------
    torch.Tensor — ángulos muestreados en [-π, π], forma [N]
    """
    mu_np      = mu.cpu().numpy()
    # numpy.random.vonmises espera μ en radianes y κ ≥ 0
    samples_np = np.random.vonmises(mu_np, kappa)
    return torch.tensor(samples_np, dtype=torch.float, device=mu.device)


def forward_diffusion(chi_0: torch.Tensor,
                      mask:  torch.Tensor,
                      t:     int) -> torch.Tensor:
    """
    Aplica el proceso forward a un tensor de ángulos chi reales.
    Solo se añade ruido a las posiciones donde mask=True
    (ángulos con valor experimental); las posiciones enmascaradas
    se dejan a 0 para no contaminar el entrenamiento.

    Parámetros
    ----------
    chi_0 : torch.Tensor — ángulos chi reales [N, 4] (radianes)
    mask  : torch.Tensor — máscara booleana    [N, 4]
    t     : int          — paso de difusión (0 ≤ t ≤ T)

    Retorna
    -------
    torch.Tensor — ángulos ruidosos χ_t [N, 4] (radianes)
    """
    kappa   = get_kappa(t)
    chi_rad = torch.deg2rad(chi_0)   # Convertimos a radianes
    chi_t   = torch.zeros_like(chi_rad)

    # Añadimos ruido solo en posiciones con ángulo real definido
    chi_t[mask] = von_mises_sample(chi_rad[mask], kappa)
    return chi_t


